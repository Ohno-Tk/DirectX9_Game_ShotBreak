/*-----------------------------------------------------------------------------
 関数名:	void UpdateTutorial( void )
 引数:		
 戻り値:	
 説明:		更新
-----------------------------------------------------------------------------*/
void UpdateTutorial( void )
{

	bool *Falg = GetFlag();

	UpdateTutorialBg();		//	チュートリアルの更新




	if( *Falg == true )
	{

		//	画面遷移
		if( GetKeyboardTrigger( DIK_SPACE ) )
		{

			//	フェードのセット
			SetFade( FADE_OUT , MODE_GAME );

		}	//	end of if

	}	//	end of if

	else if( *Falg == false )
	{

		if( GetKeyboardTrigger( DIK_SPACE ) )
		{

			*Falg = true;

		}	//	end of if

	}	//	end of else if

}	//	end of func

