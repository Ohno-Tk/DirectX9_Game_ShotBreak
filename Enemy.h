/*-----------------------------------------------------------------------------
	二重インクルード防止
-----------------------------------------------------------------------------*/
#ifndef _ENEMY_H_
#define _ENEMY_H_

/*-----------------------------------------------------------------------------
	定数定義
-----------------------------------------------------------------------------*/
#define MAX_ENEMY ( 10 )	//	モデルの最大ターゲット数

/*-----------------------------------------------------------------------------
	列挙
-----------------------------------------------------------------------------*/
//	出現位置のポイント
typedef enum
{

	TYPE_POINT1 = 0,
	TYPE_POINT2,
	TYPE_POINT3,
	TYPE_MAX

}TYPE_POINT;

/*-----------------------------------------------------------------------------
	構造体
-----------------------------------------------------------------------------*/
typedef struct
{
	WORLD World;	//	ワールド変換用変数
	D3DXVECTOR3 OldPos;	//	前の座標
	D3DXVECTOR3 Move;	//	移動量
	TYPE_POINT Point;	//	出現位置
	float Radius;	//	半径
	bool Use;	//	使用フラグ

}ENEMY;

/*-----------------------------------------------------------------------------
	プロトタイプ宣言
-----------------------------------------------------------------------------*/
void InitEnemy( void );		//	敵の初期化
void UninitEnemy( void );	//	敵の終了
void UpdateEnemy( void );	//	敵の更新
void DrawEnemy( void );		//	敵の描画

ENEMY *GetEnemyNor( void );	//	敵情報の取得

#endif